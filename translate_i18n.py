def extract_keys_and_comments(en_path):
    """Extract key-value pairs and comments from en.ts."""
    with open(en_path, "r", encoding="utf-8") as f:
        content = f.read()

    # Find all key-value pairs
    pattern = r'^\s*"([^"]+)":\s*"((?:[^"\\]|\\.)*)"'

    keys = []
    lines = content.split("\n")
    current_comment = ""

    for line in lines:
        # Track comments
        if line.strip().startswith("//"):
            current_comment = line.strip()
            continue

        match = re.match(pattern, line)
        if match:
            key = match.group(1)
            value = match.group(2)
            keys.append((key, value, current_comment))
            current_comment = ""

    return keys, content

def generate_file(lang_code, translations, en_path, output_dir):
    """Generate a language file with translated values for specified keys."""
    keys, en_content = extract_keys_and_comments(en_path)

    output_lines = []
    output_lines.append(f"const {lang_code} = {{")
    output_lines.append(f"  translation: {{")

    last_comment = ""

    for key, en_value, comment in keys:
        if comment:
            output_lines.append(f"    {comment}")

        # Use translation or keep English
        translated = translations.get(key, en_value)

        # Escape special characters for JavaScript string
        escaped = translated.replace("\\", "\\\\").replace('"', '\\"')
        output_lines.append(f'    "{key}": "{escaped}",')

    output_lines.append("  },")
    output_lines.append("};")
    output_lines.append(f"")
    output_lines.append(f"export default {lang_code};")
    output_lines.append(f"")

    output_path = os.path.join(output_dir, f"{lang_code}.ts")
    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(output_lines) + "\n")

    print(f"  Written: {output_path} ({len(translations)} translated keys)")

